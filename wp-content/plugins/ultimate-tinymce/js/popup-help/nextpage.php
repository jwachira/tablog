<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>NextPage (Page Break) Box:</strong></span>
<br />
This option will enable the NextPage (Page Break) Box.  This button will be added to the first row of the visual editor, right next to the "Insert More" button.

<br /><br />

<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
Placing a check in the box will enable the button in your visual editor.  Unchecking the box removes the button from the editor.<br /><br />
The NextPage button is a nifty little feature.  It allows you to break your post into multiple pages.  When viewing on the front-end, the user will see a sequential set of page numbers.  Clicking each number will take them to the corresponding content.<br /><br />Check the screenshots below for an example.

