<br />
This option will enable the HTML Code Box.  This button will be added to row four of the visual editor.

<br /><br />

<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
Placing a check in the box will enable the button in your visual editor.  Unchecking the box removes the button from the editor.<br /><br />
This option enables a popup box displaying your html code.  The main use I've found for this is when when switching between your visual and HTML tabs, the content can often be destroyed with the way tinymce behaves with certain code.  Using the popup option, no code is stripped from the editor and you are able to make your HTML adjustments in the popup window and save.<br /><br />

The one drawback I've found, is the HTML popup editor does not "beautify" the code.  Rather, it runs it all together.  So, I guess the lesson to be learned is you have to give something to get something...

