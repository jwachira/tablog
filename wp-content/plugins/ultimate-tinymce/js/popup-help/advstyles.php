<br /><br />

<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
Checking this option will replace the original default wordpress functionality of the styles dropdown box.  Unchecking this option will restore the default wordpress functionality.<br /><br />
The advanced version replaces the default wordpress styles with a newly created list of special styles.  My advanced styles will give you options for formatted text, borders, and alert, notification, error, and information boxes.<br /><br />
I am currently working on a way for the user to enter their own custom styles and have it populate to this dropdown box.  Hopefully, I will be able to include this feature in a future update.

